package Lesson1_Start.homeWork;

public class operation {
    static long Sum(int firstNumber, long secondNumber){
        return firstNumber+secondNumber;
    }
    static double Subtraction(float firstNumber, double secondNumber){
        return firstNumber-secondNumber;
    }
    static int Multiplication(byte firstNumber, int secondNumber){
        return firstNumber*secondNumber;
    }
    static int Division(short firstNumber, short secondNumber){
        return firstNumber/secondNumber;
    }
    static double Reminder(short firstNumber, int secondNumber){
        return firstNumber%secondNumber;
    }

}
